// OPERATOR IN C LANGUAGE //
/* ARITHMETIC OPERATOR */
#include <stdio.h>

int main()
{
    int a ,b;
    a = 3;
    b = 2;
    
    printf("a-b = %d\n" ,a-b); 
    printf("a+b = %d\n" ,a+b);    
    printf("a*b = %d\n" ,a*b); 
    printf("a/b = %d\n" ,a/b); 
    
    return 0;
}

/*OUTPUT : 
a-b = 1
a+b = 5
a*b = 6
a/b = 1
*/
